package july.ex_13072024;

public class lab029 {
    public static void main(String[] args) {
        //unary operators
        int age = 28;       //no need to assign + sign, unary +
         int temp = -12;    //unary -
        System.out.println(age);
        System.out.println(temp);

        //arithmetic operators +,/,*,-,
        // modules operator %
        System.out.println(age+ temp);
        System.out.println(age - temp);
        System.out.println(age / temp);
        System.out.println(age * temp);
        System.out.println(age % temp);  //remainder

    }
}
